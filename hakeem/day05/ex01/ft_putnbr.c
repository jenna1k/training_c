void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int		i;
	char	c[1111];

	i = 0;
	if (nb == 0)
		ft_putchar('0');
	else
	{
		if (nb < 0)
			ft_putchar('-');
		while (nb != 0)
		{
			if (nb >= 0)
				c[i] = (nb % 10 + '0');
			else
				c[i] = (nb % 10) * (-1) + '0';
			nb /= 10;
			i++;
		}
		while (i - 1 >= 0)
		{
			ft_putchar(c[i - 1]);
			i--;
		}
	}
}
