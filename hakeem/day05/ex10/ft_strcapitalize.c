char	*ft_strcapitalize(char *str)
{
	int i;

	i = -1;
	while (str[++i] != '\0')
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = (str[i] - 'A') + 'a';
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i - 1] >= '0' && str[i - 1] <= '9'))
		{
			if (str[i - 1] < 'a' || str[i - 1] > 'z')
			{
				if (str[i] >= 'a' && str[i] <= 'z')
				{
					str[i] = (str[i] - 'a') + 'A';
					i++;
				}
			}
		}
		i++;
	}
	return (str);
}
