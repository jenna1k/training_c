int        ft_atoi(char *c)
{
    int i;
    int ans;
    int flag;
    int x;
    
    i = 0;
    flag = 0;
    ans = 0;
    while (c[i] != '\0' && (c[i] == ' ' || c[i] == '\t' ||
                            c[i] == '\r' || c[i] == '\n'))
        i++;
    x = i;
    while (c[i] != '\0' && ((c[i] >= '0' && c[i] <= '9') ||
                            (i == x && (c[i] == '-' || c[i] == '+'))))
    {
        if (c[i] == '-')
            flag = 1;
        if (c[i] >= '0' && c[i] <= '9')
        {
            ans *= 10;
            ans += (c[i] - '0');
        }
        i++;
    }
    if (flag)
        ans *= -1;
    return (ans);
}
