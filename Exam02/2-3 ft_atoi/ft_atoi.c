int		ft_atoi(const char *str)
{
	int	i;
	int j;
	int number;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z') || (str[i] >= 'A' && str[i] <= 'Z'))
		{
			j = str[i] - '0';
			number = number + j;
		}
		i++;
	}
	return (number);
}
