#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	char c;

	c = '9';
	while (c >= '0')
	{
		ft_putchar(c);
		c--;
	}
	return (0);
}
